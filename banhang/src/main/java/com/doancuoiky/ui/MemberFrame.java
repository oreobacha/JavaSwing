    private void setupIconButton(JButton jbutton, String url){
        try {
            // Load ảnh từ resources
            URL iconURL = getClass().getResource(url);
            if (iconURL == null) {
                throw new IOException("Không tìm thấy ảnh trong resource");
            }

            BufferedImage originalImage = ImageIO.read(iconURL);

            BufferedImage resized = resizeImage(originalImage, 25, 30);

            jbutton.setIcon(new ImageIcon(resized));

        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    private void setupIconJabel(JLabel jlabel, String url){
        try {
            // Load ảnh từ resources
            URL iconURL = getClass().getResource(url);
            if (iconURL == null) {
                throw new IOException("Không tìm thấy ảnh trong resource");
            }

            BufferedImage originalImage = ImageIO.read(iconURL);

            BufferedImage resized = resizeImage(originalImage, 160, 160);

            jlabel.setIcon(new ImageIcon(resized));

        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    private BufferedImage resizeImage(BufferedImage img, int width, int height) {
        Image tmp = img.getScaledInstance(width, height, Image.SCALE_SMOOTH);
        BufferedImage resized = new BufferedImage(width, height, BufferedImage.TYPE_INT_ARGB);
        Graphics2D g2d = resized.createGraphics();
        g2d.drawImage(tmp, 0, 0, null);
        g2d.dispose();
        return resized;
    }

