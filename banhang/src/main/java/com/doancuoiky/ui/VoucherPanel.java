    public void reloadtableView(){
        DefaultTableModel model = (DefaultTableModel) tableKh.getModel();
        model.setRowCount(0);
        tfSearch.setText("");
        reset_filter();
        setUptableView();   
    }
    private void search_sp (String str) {
        TableRowSorter<DefaultTableModel> rowSorter;
        DefaultTableModel model = (DefaultTableModel) tableKh.getModel();
        rowSorter = new TableRowSorter<>(model);
        tableKh.setRowSorter(rowSorter);
        rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + str));
    }
    
    private void resetFieldClient(){
        tfSoLuong.setText("");
        tfKhuyenMai.setText("");
        tfFromDate.setDate(null);
        tfToDate.setDate(null);
        tfGiaTri.setText("");
        reset_filter();
    }
    
    private void reset_filter(){
        TableRowSorter<DefaultTableModel> rowSorter;
        DefaultTableModel model = (DefaultTableModel) tableKh.getModel();
        rowSorter = new TableRowSorter<>(model);
        tableKh.setRowSorter(rowSorter);
        rowSorter.setRowFilter(RowFilter.regexFilter("(?i)"));
    }
    
    public void load_voucher(){
        DefaultTableModel model = (DefaultTableModel) tableKh.getModel();
        model.setRowCount(0);
        tfSearch.setText("");
        reset_filter();
        setUptableView();  
    }
    
    private void addDataModelNewVoucher(){
        String KhuyenMai  = tfKhuyenMai.getText();
        String SoLuong  = tfSoLuong.getText();
        String tuNgay  = FileUtils.getFormattedDate(tfFromDate);
        String denNgay  = FileUtils.getFormattedDate(tfToDate);
        String GiaTri  = tfGiaTri.getText();
        VoucherData = new VoucherModel(KhuyenMai, SoLuong, tuNgay, denNgay, GiaTri);                            
    }
    
    private Boolean check_field_validate () {
        if (!tfKhuyenMai.getText().isEmpty() & !tfSoLuong.getText().isEmpty() & !tfFromDate.getDateFormatString().isEmpty() & !tfToDate.getDateFormatString().isEmpty() &  !tfGiaTri.getText().isEmpty()) {
            return true;
        }
        else {
            return false;
        }
    }
    
