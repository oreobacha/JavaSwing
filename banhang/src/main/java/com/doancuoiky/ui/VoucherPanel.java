    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        if (check_field_validate()){
            addDataModelNewVoucher();
            if (VoucherDao.getVoucherByMa(VoucherData.getMaKhuyenMai()) == null){
                if (VoucherDao.insertVoucher(VoucherData)){
                    resetFieldClient();
                    JOptionPane.showMessageDialog(null, "Đã thêm mã khuyến mại thành công");
                    reloadtableView();
                }else {
                    JOptionPane.showMessageDialog(null, "Thông tin mã khuyến mại đã tồn tại trên hệ thống vui lòng kiểm tra lại");
                }
            }else {
                JOptionPane.showMessageDialog(null, "Mã khuyến mại đã tồn tại trên hệ thống không thể thêm mới vui lòng kiểm tra lại hoặc dùng tính năng cập nhật");
            }
        }else  {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin bắt buộc trước khi thêm mới"); 
        }
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        if (check_field_validate()){
            addDataModelNewVoucher();
            if (VoucherDao.updateVoucher(VoucherData, MaKhuyenMai)){
                resetFieldClient();
                JOptionPane.showMessageDialog(null, "Đã chỉnh sửa thông tin mã khuyến mại thành công");
                reloadtableView();
            }else {
                JOptionPane.showMessageDialog(null, "Lỗi hệ thống");
            }
        }else {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin bắt buộc trước khi cập nhật"); 
        }
    }//GEN-LAST:event_btnupdateActionPerformed

