    public void setPlaceholderOnFocus(JTextField field, String placeholder) {
    if (field.getText().equals(placeholder)) {
        field.setText("");
        field.setForeground(Color.BLACK);
        }
    };

    public void resetPlaceholderOnBlur(JTextField field, String placeholder) {
    if (field.getText().isEmpty()) {
        field.setText(placeholder);
        field.setForeground(Color.GRAY);
        }
    };

